function [K_el] = K_el_fun(Jx,Iy,Iz,E,A,G,l)
K_el_tr = [E*A/l zeros(1,11);...
    0 12*E*Iz/l^3 zeros(1,10);...
    0 0 12*E*Iy/l^3 zeros(1,9);...
    0 0 0 G*Jx/l zeros(1,8);...
    0 0 -6*E*Iy/l^2 0 4*E*Iy/l zeros(1,7);...
    0 6*E*Iz/l^2 0 0 0 4*E*Iz/l zeros(1,6);...
    -E*A/l zeros(1,5) E*A/l zeros(1,5);...
    0 -12*E*Iz/l^3 0 0 0 -6*E*Iz/l^2 0 12*E*Iz/l^3 0 0 0 0;...
    0 0 -12*E*Iy/l^3 0 6*E*Iy/l^2 0 0 0 12*E*Iy/l^3 0 0 0;...
    0 0 0 -G*Jx/l zeros(1,5) G*Jx/l 0 0;...
    0 0 -6*E*Iy/l^2 0 2*E*Iy/l 0 0 0 6*E*Iy/l^2 0 4*E*Iy/l 0;...
    0 6*E*Iz/l^2 0 0 0 2*E*Iz/l 0 -6*E*Iz/l^2 0 0 0 4*E*Iz/l];
K_el=K_el_tr'+triu(K_el_tr',1)';
end

